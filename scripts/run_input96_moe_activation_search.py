def _lambda_dict(names: list[str], value: float) -> dict[str, float]:
    return {name: float(value) for name in names}


def _schedule_dict(names: list[str], value: str = "none") -> dict[str, str]:
    return {name: value for name in names}



def _selector_cfg(
    *,
    source_split: str,
    positive_sample_weight: float,
    class_weight: str | float = "auto",
    class_weight_max: float = 8.0,
    init_skip_bias: float = 0.0,
    min_rel: float = 0.0,
    use_penalty_identity: bool = False,
) -> dict[str, Any]:
    return {
        "enable": True,
        "source_split": str(source_split),
        "epochs": 30,
        "train_fraction": 0.75,
        "hidden_dim": 64,
        "batch_size": 256,
        "lr": 1.0e-3,
        "weight_decay": 1.0e-4,
        "standardize_features": True,
        "class_weight": class_weight,
        "class_weight_max": float(class_weight_max),
        "init_skip_bias": float(init_skip_bias),
        "use_penalty_identity": bool(use_penalty_identity),
        "positive_sample_weight": float(positive_sample_weight),
        "negative_sample_weight": 1.0,
        "label_min_abs_improvement": 0.0,
        "label_min_rel_improvement": float(min_rel),
    }


