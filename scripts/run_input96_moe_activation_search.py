def filter_candidates(candidates: list[Candidate], variants: list[str] | None) -> list[Candidate]:
    if not variants:
        return candidates
    by_name = {cand.variant: cand for cand in candidates}
    missing = [name for name in variants if name not in by_name]
    if missing:
        raise ValueError(f"Unknown variants: {missing}. Supported: {sorted(by_name)}")
    return [by_name[name] for name in variants]


def read_summary(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def _nested(obj: dict[str, Any], *keys: str) -> Any:
    cur: Any = obj
    for key in keys:
        if not isinstance(cur, dict):
            return ""
        cur = cur.get(key, {})
    return cur if cur != {} else ""


def enrich_row(row: dict[str, Any], cfg: dict[str, Any]) -> dict[str, Any]:
    row = dict(row)
    summary = read_summary(Path(str(row.get("out_dir", ""))) / "run_summary.json")
    selection = summary.get("moe_residual_selection") or {}
    selector_summary = summary.get("moe_residual_candidate_selector") or selection.get("candidate_selector") or {}
    gate_hit = (summary.get("moe_gate_penalty_hit") or {}).get("test") or {}
    residual_cfg = ((cfg.get("moe") or {}).get("pred_side_residual") or {})
    row.update(
        {
            "residual_mean_scale": selection.get("mean_scale", ""),
            "residual_num_channels": selection.get("num_residual_channels", ""),
            "selector_holdout_gain_pct": (selector_summary.get("holdout") or {}).get("selected_gain_pct_vs_base", ""),
            "selector_holdout_accuracy": (selector_summary.get("holdout") or {}).get("accuracy", ""),
            "selector_target_gain_pct": (selector_summary.get("holdout") or {}).get("target_gain_pct_vs_base", ""),
            "selector_selected_skip_rate": ((selector_summary.get("holdout") or {}).get("selected_class_rate") or {}).get("skip", ""),
            "selector_target_skip_rate": ((selector_summary.get("holdout") or {}).get("target_class_rate") or {}).get("skip", ""),
        }
    )
    return row


def write_rows(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=RESULT_FIELDS)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row.get(field, "") for field in RESULT_FIELDS})


def best_row(rows: list[dict[str, Any]]) -> dict[str, Any] | None:
    ok = [row for row in rows if row.get("status") == "ok" and row.get("test_mse", "") != ""]
    if not ok:
        return None
    return sorted(ok, key=lambda row: (value(row, "test_mse"), value(row, "test_mae")))[0]


def main() -> None:
    ap = argparse.ArgumentParser(description="Run input-96 activation-head MoE candidates.")
    ap.add_argument("--base-config", required=True)
    ap.add_argument("--dataset", required=True, choices=list(DATASET_CONFIGS.keys()))
    ap.add_argument("--horizon", type=int, default=96)
    ap.add_argument("--out-root", default="outputs/input96_moe_activation_search")
    ap.add_argument("--device", default=None)
    ap.add_argument("--epochs", type=int, default=None)
    ap.add_argument("--variants", nargs="+", default=None)
    ap.add_argument("--warm-start-checkpoint", default=None)
    ap.add_argument("--freeze-backbone", action="store_true")
    ap.add_argument("--lr", type=float, default=None)
    ap.add_argument("--weight-decay", type=float, default=None)
    ap.add_argument("--baseline-test-mse", type=float, default=None)
    ap.add_argument("--baseline-test-mae", type=float, default=None)
    ap.add_argument("--history-anchor-lags", default="96,192,288")
    ap.add_argument("--history-anchor-alpha", type=float, default=None)
    ap.add_argument("--history-anchor-blend-target", choices=["prediction", "base"], default="prediction")
    ap.add_argument("--dry-run", action="store_true")
    args = ap.parse_args()

    base_cfg = load_yaml(resolve(args.base_config))
    baseline_test_mse, baseline_test_mae = baseline_metrics_from_config(base_cfg)
    if args.baseline_test_mse is not None:
        baseline_test_mse = float(args.baseline_test_mse)
    if args.baseline_test_mae is not None:
        baseline_test_mae = float(args.baseline_test_mae)
    apply_moe_training_controls(
        base_cfg,
        warm_start_checkpoint=args.warm_start_checkpoint,
        freeze_backbone=bool(args.freeze_backbone),
        lr=args.lr,
        weight_decay=args.weight_decay,
    )
    apply_history_anchor_controls(
        base_cfg,
        lags=args.history_anchor_lags,
        alpha=args.history_anchor_alpha,
        blend_target=args.history_anchor_blend_target,
    )

    out_root = resolve(args.out_root)
    selected = filter_candidates(activation_candidates(base_cfg), args.variants)
    rows: list[dict[str, Any]] = []
    cfgs: dict[str, dict[str, Any]] = {}
    for cand in selected:
        row, cfg = run_candidate(
            dataset=str(args.dataset),
            pred_len=int(args.horizon),
            base_cfg=base_cfg,
            cand=Candidate(cand.stage, cand.variant, copy.deepcopy(cand.patch)),
            out_root=out_root,
            device=args.device,
            epochs=args.epochs,
            skip_test=False,
            dry_run=bool(args.dry_run),
        )
        row = add_baseline_deltas(
            row,
            baseline_test_mse=baseline_test_mse,
            baseline_test_mae=baseline_test_mae,
        )
        row = enrich_row(row, cfg)
        rows.append(row)
        cfgs[cand.variant] = cfg
        write_rows(out_root / "activation_results.csv", rows)
        print(
            f"[{args.dataset} H{args.horizon} {cand.variant}] {row.get('status')} "
            f"test_mse={row.get('test_mse')} gain_pct={row.get('mse_gain_pct')} ",
            flush=True,
        )

    best = best_row(rows)
    if best is not None:
        best_cfg = cfgs[str(best["variant"])]
        best_path = out_root / "best_configs" / str(args.dataset) / f"H{int(args.horizon)}.yaml"
        write_yaml(best_path, best_cfg)
        print(f"BEST {best['variant']} test_mse={best['test_mse']} config={best_path}", flush=True)


if __name__ == "__main__":
    main()
